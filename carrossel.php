<p class ="pdestaque">	
"Lista de treinos e seus objetivos"</p>
<p class ="pcentral">

1. Treino de Força:
   - Objetivo: Aumentar a massa muscular, melhorar a força e a resistência.
   
2. Treino de Resistência Cardiovascular:
   - Objetivo: Melhorar a saúde cardiovascular, aumentar a resistência aeróbica e queimar calorias.
   
3. Treino de Flexibilidade:
   - Objetivo: Aumentar a amplitude de movimento, prevenir lesões e melhorar a postura.
   
4. Treino de Potência:
   - Objetivo: Melhorar a explosão muscular, a velocidade e a agilidade.
   
5. Treino de Equilíbrio e Coordenação:
   - Objetivo: Aumentar o equilíbrio, a estabilidade e a coordenação motora.
   
6. Treino Funcional:
   - Objetivo: Melhorar o desempenho nas atividades diárias, fortalecer os músculos estabilizadores e prevenir lesões.
   
7. Treino de Hipertrofia:
   - Objetivo: Aumentar o tamanho muscular e a definição muscular.
   
8. Treino de Perda de Peso:
   - Objetivo: Queimar calorias, aumentar o metabolismo e reduzir a gordura corporal.

Cada tipo de treino tem seu foco específico, mas é importante adaptar o programa de treinamento de acordo com os objetivos individuais e as necessidades do corpo.</p>
